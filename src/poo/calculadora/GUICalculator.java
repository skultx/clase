    private void jButtonSumarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSumarActionPerformed
        // TODO add your handling code here:
        clearErrors();
        try {
            double num1 = Double.parseDouble(this.jTextFieldNum1.getText());
            try {
                double num2 = Double.parseDouble(this.jTextFieldNum2.getText());
                //todo ha ido bien y tengo valores numéricos
                double resultado = Calculadora.suma(num1, num2);
                this.jTextFieldResultado.setText(String.valueOf(resultado));
            } catch (java.lang.NumberFormatException ex) {
                this.jLabelError.setText("ERROR. Dato no numérico");
                this.jTextFieldNum2.requestFocus();
                this.jTextFieldNum2.selectAll();
            }
        } catch (java.lang.NumberFormatException ex) {
            this.jLabelError.setText("ERROR. Dato no numérico");
            this.jTextFieldNum1.requestFocus();
            this.jTextFieldNum1.selectAll();
        }

    }//GEN-LAST:event_jButtonSumarActionPerformed

