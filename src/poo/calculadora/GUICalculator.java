    private void jButtonDividirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDividirActionPerformed
        // TODO add your handling code here:
        clearErrors();
        double num1 = Double.parseDouble(this.jTextFieldNum1.getText());
        double num2 = Double.parseDouble(this.jTextFieldNum2.getText());
        if (num2 == 0) {
            this.jLabelError.setText("Divisor nulo");
        }
        double resultado = Calculadora.division(num1, num2);
        this.jTextFieldResultado.setText(String.valueOf(resultado));
    }//GEN-LAST:event_jButtonDividirActionPerformed

