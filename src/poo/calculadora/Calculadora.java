package com.gf.poo.calculadora;

/**
 *
 * @author fuehered
 */
public class Calculadora {

    //Metodos
    static public double suma(double a, double b) {
        return a + b;
    }

    static public double resta(double a, double b) {
        return a - b;
    }

    static public double producto(double a, double b) {
        return a * b;
    }

    static public double division(double a, double b) {
        return a / b;
    }

    static public double resto(double a, double b) {
        return a % b;
    }

}
