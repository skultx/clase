    private void jButtonSumarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSumarActionPerformed
        try {
            double n1 = Double.parseDouble(this.jTextField1.getText());
            double n2 = Double.parseDouble(this.jTextField2.getText());
            this.jTextFieldResultado.setText(String.valueOf(Calculadora.sumar(n1, n2)));
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe introducir datos numéricos", "Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButtonSumarActionPerformed

    private void jButtonDividirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDividirActionPerformed
        try {
            double n1 = Double.parseDouble(this.jTextField1.getText());
            double n2 = Double.parseDouble(this.jTextField2.getText());
            this.jTextFieldResultado.setText(String.valueOf(Calculadora.dividir(n1, n2)));
            this.jTextFieldResultado.setForeground(Color.WHITE);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe introducir datos numéricos", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            this.jTextFieldResultado.setForeground(Color.RED);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDividirActionPerformed

