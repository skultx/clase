    private void jTextField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1FocusLost
        //si borro el contenido
        if (this.jTextField1.getText().isEmpty()) {
            this.jTextField1.setText(String.valueOf(0));
        } else { //controlo que es numérico
            try {
                Double.parseDouble(this.jTextField1.getText());
                this.jTextField1.setToolTipText(null);
            } catch (NumberFormatException ex) {
                this.jTextField1.requestFocus();
                this.jTextField1.setToolTipText("Debe introducir un dato numérico");
            }
        }
    }//GEN-LAST:event_jTextField1FocusLost

