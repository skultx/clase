    private void botonImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonImprimirActionPerformed
        try {
            String nombreArchivo=this.cajaArchivo.getText();
            Archivo archivo=new Archivo(nombreArchivo, new Random().nextInt(100,1000));
            aniadirCola(archivo);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonImprimirActionPerformed

    private void jListColaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListColaMouseClicked
       List<Archivo> listaSeleccion= this.jListCola.getSelectedValuesList();
       int bytes=0;
       for(Archivo archivo :listaSeleccion){
            bytes+=archivo.getTamanio();
        }
       this.jLabelTamanio.setText("Bytes: " + bytes);
    }//GEN-LAST:event_jListColaMouseClicked

