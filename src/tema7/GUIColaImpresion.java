    private void botonAtenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAtenderActionPerformed
        atenderCola();
        actualizarLista();
    }//GEN-LAST:event_botonAtenderActionPerformed

    
    
    private void aniadirCola (Archivo archivo){
        cola.addLast(archivo);
        //prueba
        System.out.println(cola);
        //
        actualizarLista();
    }
    
    private void atenderCola(){
        if(!cola.isEmpty()){
            Archivo archivo =cola.removeFirst();
            System.out.println("Imprimiendo: " + archivo.getNombreArchivo());
        }
    }
    
    private void actualizarLista(){
        modeloLista.clear();
        this.jListCola.setModel(modeloLista);
        for(Archivo archivo :cola){
            modeloLista.addElement(archivo);
        }
    }
    
    
    
